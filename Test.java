    static long countTime(long edgecount, long t, long c){
        long ans = 0;
        for(int i=1;i<=edgecount;i++){
            ans = (ans + c);
            long k = ans / t;
            if(k%2!=0 && i!=edgecount){
                ans = (k+1)*t;
            }
        }

        return ans;
    }
    static long timeRequired(long n, long t, long c){
        long ans;
        if(t <= c) {
            if(c % t == 0 && (c/t)%2 == 0)
                ans = n*c;
            else
                ans = n * c + (n - 1) * (t - c % t);
        }
        else {
            long div = t/c;
            if(t % c != 0)
                div++;

            if(n % div != 0)
                ans = n * c + (n / div) * (t - (div * c) % t);
            else
                ans = n * c + ((n-1)/ div) * (t - (div * c) % t);
        }

        return ans;
    }
